#include<iostream>

using namespace std;

main(){
    int a =6;
    float b = 4;

    float result;
    //operator +, -, *, /

    //cout<<a+b;
    result = a+b;
    cout<<a<<" + "<< b <<" = "<<result<<endl;

    //min
    result=a-b;
    cout<<a<<" - "<<b<<" = "<< result<<endl;

    //X
    result= a * b ;
    cout<<a<<" X "<<b<<" = "<< result<<endl;

    // :
    result = a / b;
    cout<< a<< " / "<< b<<" = "<< result;


    return 0;
}
